     */
    public function store()
    {
        $validData = request()->validate([
            'name' => 'required|min:3|max:255'
        ]);

        $validData['user_id'] = auth()->id();

        Category::create($validData);

        return redirect(route('categories'))
            ->with('flash', 'Category successfully created!');
    }

